package com.syntax.class07;

public class IncrementAndDecrement {

	public static void main(String[] args) {
		
		int num=10;
		num=num+1;
		num+=1;
		num++;
		System.out.println(num);//13
		//10++;- Invalid argument to operation ++/--
		
		num++;
		System.out.println(num);
		
		num--;
		num--;
		num--;
		System.out.println(num);
		

	}

}
