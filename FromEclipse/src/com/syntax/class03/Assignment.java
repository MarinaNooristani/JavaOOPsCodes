package com.syntax.class03;

public class Assignment {

	public static void main(String[] args) {
		// Increase
		int num = 200;
		num = num + 500;
		System.out.println(num);// 700
		num += 200;
		System.out.println(num);// 900
		// Decrease
		int num1 = 360;
		num1 -= 60;
		System.out.println(num1);// 300
		// Divide
		int cakePiece = 11;
		cakePiece /= 4;
		System.out.println(cakePiece);// 2
		// Module
		int cake = 25;
		cake %= 7;
		System.out.println(cake);// 4

	}

}
